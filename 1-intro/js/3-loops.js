"use strict";

// for schleife
console.log('einen "for"-Loop');
var i = 1;
var ende = 3;
for (i; i<ende; i++) {
  console.log('Var i:' + i);
}


// while schleife
console.log('einen "while"-Loop');
var x = 0;
while (x < 3) {
  x = x + 1; // gleich wie: i++;
  console.log('Var x:' + x);
}

